class TrainTransform:
    def __call__(self, image, mask):
        do_scale = not (CFG["overfit_mode"] and CFG["overfit_disable_scale"])
        do_hflip = not (CFG["overfit_mode"] and CFG["overfit_disable_hflip"])
        do_cj = not (CFG["overfit_mode"] and CFG["overfit_disable_color_jitter"])

        if do_scale:
            lo, hi = CFG["scale_range"]
            ratio = random.uniform(lo, hi)
            nw = max(32, int(image.size[0] * ratio))
            nh = max(32, int(image.size[1] * ratio))
            image = image.resize((nw, nh), Image.BILINEAR)
            mask = mask.resize((nw, nh), Image.NEAREST)

        if do_hflip and random.random() < CFG["hflip_prob"]:
            image = TF.hflip(image)
            mask = TF.hflip(mask)

        if do_cj:
            image = maybe_color_jitter(image)

        crop_h, crop_w = CFG["train_size"]
        pad_h = max(0, crop_h - image.size[1])
        pad_w = max(0, crop_w - image.size[0])

        if pad_h > 0 or pad_w > 0:
            image = TF.pad(image, [0, 0, pad_w, pad_h], fill=0)
            mask = TF.pad(mask, [0, 0, pad_w, pad_h], fill=CFG["ignore_index"])

        i, j, h, w = T.RandomCrop.get_params(image, output_size=(crop_h, crop_w))
        image = TF.crop(image, i, j, h, w)
        mask = TF.crop(mask, i, j, h, w)

        image = TF.to_tensor(image)
        image = TF.normalize(image, mean=MEAN, std=STD)

        mask = torch.from_numpy(np.array(mask, dtype=np.int64))
        invalid = (mask < 0) | (mask >= CFG["num_classes"])
        mask[invalid] = CFG["ignore_index"]

        return image, mask

class ValTransform:
    def __call__(self, image, mask):
        if CFG["val_size"] is not None:
            vh, vw = CFG["val_size"]
            image = image.resize((vw, vh), Image.BILINEAR)
            mask = mask.resize((vw, vh), Image.NEAREST)

        image = TF.to_tensor(image)
