def poly_lr(base_lr, cur_iter, max_iter, power=0.9):
    return base_lr * ((1 - float(cur_iter) / max_iter) ** power)

def set_lr(optimizer, lr):
    for param_group in optimizer.param_groups:
        param_group["lr"] = lr

