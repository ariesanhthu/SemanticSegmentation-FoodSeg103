@torch.no_grad()
def fast_hist(pred, target, num_classes, ignore_index=255):
    mask = target != ignore_index
    pred = pred[mask]
    target = target[mask]
    k = (target >= 0) & (target < num_classes)
    inds = num_classes * target[k].to(torch.int64) + pred[k]
    hist = torch.bincount(inds, minlength=num_classes**2).reshape(num_classes, num_classes)
    return hist

@torch.no_grad()
def compute_scores(hist):
    hist = hist.float()
    diag = torch.diag(hist)
    gt_sum = hist.sum(dim=1)
    pred_sum = hist.sum(dim=0)
    total = hist.sum().clamp_min(1.0)

    aAcc = diag.sum() / total
    acc_cls = diag / gt_sum.clamp_min(1.0)
    iu = diag / (gt_sum + pred_sum - diag).clamp_min(1.0)

    present = gt_sum > 0
    mAcc = acc_cls[present].mean() if present.any() else torch.tensor(0.0, device=hist.device)
    mIoU = iu[present].mean() if present.any() else torch.tensor(0.0, device=hist.device)

    fg_present = present.clone()
    if CFG["background_id"] < len(fg_present):
        fg_present[CFG["background_id"]] = False

    mAcc_fg = acc_cls[fg_present].mean() if fg_present.any() else torch.tensor(0.0, device=hist.device)
    mIoU_fg = iu[fg_present].mean() if fg_present.any() else torch.tensor(0.0, device=hist.device)

    return {
        "aAcc": float(aAcc.item()),
        "mAcc": float(mAcc.item()),
        "mIoU": float(mIoU.item()),
        "mAcc_fg": float(mAcc_fg.item()),
        "mIoU_fg": float(mIoU_fg.item()),
        "iou_per_class": iu.cpu().numpy(),
        "acc_per_class": acc_cls.cpu().numpy(),
        "gt_sum": gt_sum.cpu().numpy(),
        "pred_sum": pred_sum.cpu().numpy(),
    }

