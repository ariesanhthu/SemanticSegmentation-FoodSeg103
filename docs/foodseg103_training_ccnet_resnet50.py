class ConvBNReLU(nn.Module):
    def __init__(self, in_ch, out_ch, k=3, s=1, p=1, d=1):
        super().__init__()
        self.block = nn.Sequential(
            nn.Conv2d(in_ch, out_ch, kernel_size=k, stride=s, padding=p, dilation=d, bias=False),
            nn.BatchNorm2d(out_ch),
            nn.ReLU(inplace=True),
        )
    def forward(self, x):
        return self.block(x)

def INF(B, H, W, device):
    # mask diagonal for row attention
    return -torch.diag(torch.full((H,), 1e9, device=device), diagonal=0).unsqueeze(0).repeat(B * W, 1, 1)

class CrissCrossAttention(nn.Module):
    def __init__(self, in_dim):
        super().__init__()
        inter_dim = max(1, in_dim // 8)
        self.query_conv = nn.Conv2d(in_dim, inter_dim, kernel_size=1)
        self.key_conv = nn.Conv2d(in_dim, inter_dim, kernel_size=1)
        self.value_conv = nn.Conv2d(in_dim, in_dim, kernel_size=1)
        self.softmax = nn.Softmax(dim=3)
        self.gamma = nn.Parameter(torch.zeros(1))

    def forward(self, x):
        m_batchsize, _, height, width = x.size()
        proj_query = self.query_conv(x)
        proj_key = self.key_conv(x)
        proj_value = self.value_conv(x)

        # H attention
        proj_query_H = proj_query.permute(0, 3, 1, 2).contiguous().view(m_batchsize * width, -1, height).permute(0, 2, 1)
        proj_key_H = proj_key.permute(0, 3, 1, 2).contiguous().view(m_batchsize * width, -1, height)
        proj_value_H = proj_value.permute(0, 3, 1, 2).contiguous().view(m_batchsize * width, -1, height)

        energy_H = torch.bmm(proj_query_H, proj_key_H) + INF(m_batchsize, height, width, x.device)
        energy_H = energy_H.view(m_batchsize, width, height, height).permute(0, 2, 1, 3)

        # W attention
        proj_query_W = proj_query.permute(0, 2, 1, 3).contiguous().view(m_batchsize * height, -1, width).permute(0, 2, 1)
        proj_key_W = proj_key.permute(0, 2, 1, 3).contiguous().view(m_batchsize * height, -1, width)
        proj_value_W = proj_value.permute(0, 2, 1, 3).contiguous().view(m_batchsize * height, -1, width)

        energy_W = torch.bmm(proj_query_W, proj_key_W)
        energy_W = energy_W.view(m_batchsize, height, width, width)

        concate = self.softmax(torch.cat([energy_H, energy_W], dim=3))

        att_H = concate[:, :, :, 0:height].permute(0, 2, 1, 3).contiguous().view(m_batchsize * width, height, height)
        att_W = concate[:, :, :, height:height + width].contiguous().view(m_batchsize * height, width, width)

        out_H = torch.bmm(proj_value_H, att_H.permute(0, 2, 1))
        out_H = out_H.view(m_batchsize, width, -1, height).permute(0, 2, 3, 1)

        out_W = torch.bmm(proj_value_W, att_W.permute(0, 2, 1))
        out_W = out_W.view(m_batchsize, height, -1, width).permute(0, 2, 1, 3)

        out = self.gamma * (out_H + out_W) + x
        return out

class RCCAModule(nn.Module):
    def __init__(self, in_channels, out_channels, recurrence=2):
        super().__init__()
        self.recurrence = recurrence
        self.conva = nn.Sequential(
            nn.Conv2d(in_channels, out_channels, kernel_size=3, padding=1, bias=False),
            nn.BatchNorm2d(out_channels),
            nn.ReLU(inplace=True),
        )
        self.cca = CrissCrossAttention(out_channels)
        self.convb = nn.Sequential(
            nn.Conv2d(out_channels, out_channels, kernel_size=3, padding=1, bias=False),
            nn.BatchNorm2d(out_channels),
            nn.ReLU(inplace=True),
        )

    def forward(self, x):
        out = self.conva(x)
        for _ in range(self.recurrence):
            out = self.cca(out)
        out = self.convb(out)
        return out

class FCNHead(nn.Module):
    def __init__(self, in_channels, channels, num_classes, dropout=0.1):
        super().__init__()
        self.block = nn.Sequential(
            nn.Conv2d(in_channels, channels, kernel_size=3, padding=1, bias=False),
            nn.BatchNorm2d(channels),
            nn.ReLU(inplace=True),
            nn.Dropout2d(dropout),
            nn.Conv2d(channels, num_classes, kernel_size=1),
        )
    def forward(self, x):
        return self.block(x)

class ResNetDilatedBackbone(nn.Module):
    def __init__(self, pretrained=True):
        super().__init__()
        weights = ResNet50_Weights.IMAGENET1K_V2 if pretrained else None
        base = models.resnet50(weights=weights, replace_stride_with_dilation=[False, True, True])

        self.conv1 = base.conv1
        self.bn1 = base.bn1
        self.relu = base.relu
        self.maxpool = base.maxpool
        self.layer1 = base.layer1
        self.layer2 = base.layer2
        self.layer3 = base.layer3
        self.layer4 = base.layer4

        self.out_channels = 2048
        self.aux_channels = 1024

    def forward(self, x):
        x = self.conv1(x)
        x = self.bn1(x)
        x = self.relu(x)
        x = self.maxpool(x)     # /4
        x = self.layer1(x)      # /4
        x = self.layer2(x)      # /8
        aux = self.layer3(x)    # /8 with dilation
        out = self.layer4(aux)  # /8 with dilation
        return out, aux

class CCNetSeg(nn.Module):
    def __init__(self, num_classes, recurrence=2, pretrained=True, aux=True, dropout=0.1):
        super().__init__()
        self.backbone = ResNetDilatedBackbone(pretrained=pretrained)
        self.rcca = RCCAModule(self.backbone.out_channels, CFG["rcca_channels"], recurrence=recurrence)
        self.cls_head = FCNHead(CFG["rcca_channels"], 512, num_classes, dropout=dropout)
        self.use_aux = aux
        if self.use_aux:
            self.aux_head = FCNHead(self.backbone.aux_channels, 256, num_classes, dropout=dropout)

    def forward(self, x):
        input_size = x.shape[-2:]
        feat, aux_feat = self.backbone(x)
        feat = self.rcca(feat)
        logits = self.cls_head(feat)
        logits = F.interpolate(logits, size=input_size, mode="bilinear", align_corners=False)

        if self.use_aux:
            aux_logits = self.aux_head(aux_feat)
            aux_logits = F.interpolate(aux_logits, size=input_size, mode="bilinear", align_corners=False)
            return logits, aux_logits
        return logits

