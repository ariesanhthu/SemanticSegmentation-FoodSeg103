def overlay_mask_on_image(image_np, mask_np, alpha=0.45):
    """
    image_np: H,W,3 float [0,1]
    mask_np : H,W int
    """
    color_mask = decode_mask(torch.from_numpy(mask_np) if not isinstance(mask_np, torch.Tensor) else mask_np.cpu())
    color_mask = color_mask.astype(np.float32) / 255.0
    over = image_np * (1 - alpha) + color_mask * alpha
    return np.clip(over, 0, 1)

@torch.no_grad()
def visualize_test_model(
    loader,
    ckpt_path=None,
    num_samples=8,
    save_name="test_model_visualization.png",
    show_overlay=True,
):
    if ckpt_path is None:
        ckpt_path = BEST_CKPT if BEST_CKPT.exists() else LAST_CKPT

    if not Path(ckpt_path).exists():
        raise FileNotFoundError(f"Checkpoint not found: {ckpt_path}")

    load_model_for_eval(ckpt_path)

    samples = []
    pbar = tqdm(loader, total=len(loader), desc="Visualize test model")

    for images, masks, stems, img_paths, mask_paths in pbar:
        images = images.to(CFG["device"], non_blocking=True)

        outputs = model(images)
        if isinstance(outputs, tuple):
            logits = outputs[0]
        else:
            logits = outputs

        preds = torch.argmax(logits, dim=1).cpu()

        for b in range(images.size(0)):
            img = denorm_image(images[b].detach().cpu()).permute(1, 2, 0).numpy()
            gt = masks[b].cpu().numpy()
            pdm = preds[b].cpu().numpy()

            item = {
                "image": img,
                "gt": gt,
                "pred": pdm,
                "stem": stems[b],
            }

            if show_overlay:
                item["overlay_gt"] = overlay_mask_on_image(img, gt)
                item["overlay_pred"] = overlay_mask_on_image(img, pdm)

            samples.append(item)
            if len(samples) >= num_samples:
                break

        if len(samples) >= num_samples:
            break

    if len(samples) == 0:
        print("[WARN] No samples collected for visualization.")
        return

    # layout:
    # no overlay  -> image | gt | pred
    # overlay     -> image | gt | pred | overlay_gt | overlay_pred
    ncols = 5 if show_overlay else 3
    fig, axes = plt.subplots(len(samples), ncols, figsize=(4 * ncols, 4 * len(samples)))

    if len(samples) == 1:
        axes = np.expand_dims(axes, 0)

    for i, item in enumerate(samples):
        axes[i, 0].imshow(item["image"])
        axes[i, 0].set_title(f"{item['stem']} - image")
        axes[i, 1].imshow(decode_mask(torch.from_numpy(item["gt"])))
        axes[i, 1].set_title("ground truth")
        axes[i, 2].imshow(decode_mask(torch.from_numpy(item["pred"])))
        axes[i, 2].set_title("prediction")

        if show_overlay:
            axes[i, 3].imshow(item["overlay_gt"])
            axes[i, 3].set_title("overlay gt")
            axes[i, 4].imshow(item["overlay_pred"])
            axes[i, 4].set_title("overlay pred")

        for j in range(ncols):
            axes[i, j].axis("off")

    plt.tight_layout()
    save_path = PATHS["work_dir"] / save_name
    plt.savefig(save_path, dpi=150, bbox_inches="tight")
    plt.show()
    plt.close(fig)

    print(f"[SAVED] {save_path}")

